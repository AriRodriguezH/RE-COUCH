package com.example.proyectoi.entidades;

public class ConsultarRutinas {
    private int idrutina;
    String nombreRutina;
    String Nivel;
    String NombreEjercicio;
    String DiaSemana;
    String ParteATrabajar;
    String PesoAgregado;
    String Series;
    String NumRepeticion;
    String nombreMaquina;

    public Integer getIdrutina() {
        return idrutina;
    }

    public void setIdrutina(Integer idrutina) {
        this.idrutina = idrutina;
    }

    public String getNombreRutina() {
        return nombreRutina;
    }

    public void setNombreRutina(String nombreRutina) {
        this.nombreRutina = nombreRutina;
    }

    public String getNivel() {
        return Nivel;
    }

    public void setNivel(String nivel) {
        Nivel = nivel;
    }

    public String getNombreEjercicio() {
        return NombreEjercicio;
    }

    public void setNombreEjercicio(String nombreEjercicio) {
        NombreEjercicio = nombreEjercicio;
    }

    public String getDiaSemana() {
        return DiaSemana;
    }

    public void setDiaSemana(String diaSemana) {
        DiaSemana = diaSemana;
    }

    public String getParteATrabajar() {
        return ParteATrabajar;
    }

    public void setParteATrabajar(String parteATrabajar) {
        ParteATrabajar = parteATrabajar;
    }

    public String getPesoAgregado() {
        return PesoAgregado;
    }

    public void setPesoAgregado(String pesoAgregado) {
        PesoAgregado = pesoAgregado;
    }

    public String getSeries() {
        return Series;
    }

    public void setSeries(String series) {
        Series = series;
    }

    public String getNumRepeticion() {
        return NumRepeticion;
    }

    public void setNumRepeticion(String numRepeticion) {
        NumRepeticion = numRepeticion;
    }

    public String getNombreMaquina() {
        return nombreMaquina;
    }

    public void setNombreMaquina(String nombreMaquina) {
        this.nombreMaquina = nombreMaquina;
    }
}
