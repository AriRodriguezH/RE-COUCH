package com.example.proyectoi.entidades;

public class ConsultaHistoricoAsesorado {
    private String nombreHA;
    private String apellidopHA;
    private String apellidomHA;
    private String tallaHA;
    private String pesoHA;
    private String alturaHA;
    private String FechaCreacionHA;

    public String getNombreHA() {
        return nombreHA;
    }

    public void setNombreHA(String nombreHA) {
        this.nombreHA = nombreHA;
    }

    public String getApellidopHA() {
        return apellidopHA;
    }

    public void setApellidopHA(String apellidopHA) {
        this.apellidopHA = apellidopHA;
    }

    public String getApellidomHA() {
        return apellidomHA;
    }

    public void setApellidomHA(String apellidomHA) {
        this.apellidomHA = apellidomHA;
    }

    public String getTallaHA() {
        return tallaHA;
    }

    public void setTallaHA(String tallaHA) {
        this.tallaHA = tallaHA;
    }

    public String getPesoHA() {
        return pesoHA;
    }

    public void setPesoHA(String pesoHA) {
        this.pesoHA = pesoHA;
    }

    public String getAlturaHA() {
        return alturaHA;
    }

    public void setAlturaHA(String alturaHA) {
        this.alturaHA = alturaHA;
    }

    public String getFechaCreacionHA() {
        return FechaCreacionHA;
    }

    public void setFechaCreacionHA(String fechaCreacionHA) {
        FechaCreacionHA = fechaCreacionHA;
    }
}
